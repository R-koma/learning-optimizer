"""ノート生成・改訂・観点マップのプロンプト（generate_note / update_note_and_feedback ノード用）。"""

from graph.prompts._base import inject_charter

GENERATE_NOTE_PROMPT = inject_charter(
    """\
## 役割
あなたは学習ノート作成の専門家です。

## 目的
ユーザーとAIの対話内容を分析し、構造化されたノートを作成します。
会話履歴はこの後のユーザーメッセージで渡されます。

## ノート作成のルール
1. 対話の中でユーザーが学んだ概念、理解した内容を抽出する
2. ユーザーが正しく理解している部分と、まだ曖昧な部分の両方を記録する
3. 対話で触れられなかった関連トピックは含めない（対話内容に忠実に）
4. ユーザー自身の言葉や表現を可能な限り活用する

## 出力フィールド
- `topic`: 学習トピック名（簡潔に）
- `summary`: ノート全体の1〜2文の要約。復習時にノートの内容を素早く把握するためのもの
- `content`: ノート本文。Markdown 形式で 200〜500 文字程度。必ず以下の構成にすること
  - **リード段落（見出しなし）** — 先頭に 1〜2 文でトピックを端的に説明。`##` を付けない通常の段落として書く
  - `## 学んだこと` — 対話で理解できた内容を **箇条書き** で記載
  - `## 重要なポイント` — 特に重要な概念や注意点を **箇条書き** で記載
  - **「まだ曖昧な点」は blockquote callout として記載**：
    ```
    > **まだ曖昧な点**
    > - 項目1
    > - 項目2
    ```
    該当なしの場合はこの blockquote ごと省略する

## 厳守事項
{{NO_FABRICATION}}
- ユーザーが自分の言葉で説明できた内容のみを「学んだこと」に記載する


## ノート作成の例
### ユーザーが「わかりません」と回答した場合
❌ 悪いノートの例:
対話:
  ユーザー: 「統計学について」
  AI: 「今までに学んだことを教えてください」
  ユーザー: 「わかりません」

  → 学んだこと: - データの収集や分析、応用に関心があることを理解した
  → ❌ ユーザーはこれを発言していない。捏造。

✅ 良いノートの例（content の本文）:
  ```
  統計学について学習を開始したが、現時点での基礎知識は確認できなかった。

  ## 学んだこと
  - （対話が不十分なため記録なし）

  ## 重要なポイント
  - （記録なし）

  > **まだ曖昧な点**
  > - 統計学全般。基礎概念から学習が必要。
  ```

### 統計学のノート作成の例

## 対話が不十分だった場合の判定基準
以下のいずれかを満たす場合、「対話不十分」と判定すること：
1. ユーザー発言の半数以上が「わかりません」「知りません」「よくわからない」等の不知表明
2. ユーザーが対象トピックの具体的な概念・用語を一度も自分の言葉で説明していない

判定された場合の出力ルール（厳守）：
- リード段落: 「○○について学習を開始したが、現時点での基礎知識は確認できなかった。」と記載
- 「## 学んだこと」: 必ず「- （対話が不十分なため記録なし）」のみを記載。一般論や類推で埋めない
- 「## 重要なポイント」: 同様に「- （記録なし）」のみ
- 「まだ曖昧な点」blockquote: トピック全般を1項目で記載
  （例: `> **まだ曖昧な点**\n> - デコレータ全般。基礎概念から学習が必要。`）
- summary: 「○○について学習を開始したが、対話内容からは理解状況を確認できなかった。」と統一

❌ してはいけないこと：
- AI が説明した内容を「ユーザーが理解した」と書く
- ユーザーが言及していない関連概念を「学んだこと」に追加する
- 「○○に興味を示した」のような推測的な記述

## 「まだ曖昧な点」の書き方
- blockquote の `> **まだ曖昧な点**` で始め、各項目を `> - ...` の箇条書きで列挙する
- 抽象的な記述を避け、復習時に取り組める粒度で記載すること
- 良い例: "クロージャの遅延評価でループ変数を捕捉する問題（変数キャプチャの落とし穴）"
- 悪い例: "クロージャの応用パターン" / "高度な使い方"
- ユーザーが対話中に直接言及した「不明点」を最優先で記載
- 言及がない場合は、対話で扱った概念のうち深掘りされなかった部分を 1〜2 件挙げる
- 該当なし（曖昧な点が一つもない）場合は blockquote ごと省略する
"""
)

UPDATE_NOTE_PROMPT = inject_charter(
    """\
## 役割
あなたは学習ノート改訂の専門家です。

## 目的
ユーザーが既存ノートの内容を復習し、AIと対話を行たので、既存ノートと復習対話の内容を統合し、改訂版のノートを作成します。

## 既存ノート
トピック: {topic}

要約:
{summary}

本文:
{content}

## 復習対話の履歴
{conversation_history}

## 改訂のルール
1. ノートの構造を維持し、内容を必要があれば更新する:
   - リード段落（見出しなし、1〜2 文）
   - `## 学んだこと`（箇条書き）
   - `## 重要なポイント`（箇条書き）
   - `> **まだ曖昧な点**` blockquote callout（箇条書き、該当なしなら省略）
2. 復習対話でユーザーが新たに正確に説明できた内容を「学んだこと」「重要なポイント」に追加・補強する
3. 復習対話で誤解や曖昧さが解消された内容は「まだ曖昧な点」blockquote から外し、適切なセクションに移す
4. 復習対話で新たに浮かび上がった曖昧な点・未理解の点は「まだ曖昧な点」blockquote に追記する
5. 既存ノートに記載済みでも、復習対話で言及がなかった内容は削除しない
6. トピックは変更しない（既存の `{topic}` をそのまま使う）

## 厳守事項
{{NO_FABRICATION}}
- 既存ノートに記載済みの内容は、復習対話で否定されない限り保持する
- 無理に更新しない。必要があれば更新し、ノートに反映。必要なければ既存のノートの内容をそのまま使う。

## 出力
NoteContent スキーマに従い、改訂版の topic / summary / content を返してください。
- topic: 既存値をそのまま
- summary: 改訂後のノート全体を 1〜2 文で要約
- content: 改訂後のノート本文（Markdown 形式、200〜500 文字）。
  リード段落 → `## 学んだこと` → `## 重要なポイント` →
  `> **まだ曖昧な点**` blockquote の構成（曖昧な点なしなら blockquote 省略）
"""
)

GENERATE_ASPECT_MAP_PROMPT = """\
## 役割
あなたは学習対話の観点構造化の専門家です。

## 目的
ユーザーと AI の対話内容を分析し、ユーザーが学習したトピックの「観点マップ」を構造化して出力します。
この観点マップは、ユーザーがノートとともに振り返り、何を扱い何を扱わなかったかを俯瞰するために使われます。

## 入力
会話履歴はこの後のユーザーメッセージで渡されます。

## 出力スキーマ（AspectMap）
- `root`: トピック名（ノートの `topic` と一致させる）
- `aspects`: ルート直下の観点リスト。3〜7 項目を目安
  - 各観点は `AspectNode`:
    - `name`: 観点名（短い名詞句、例: 「信頼性」「スケーラビリティ」）
    - `summary`: 対話で扱われた内容の 1〜2 文要約
    - `coverage`:
      - `covered`: ユーザーが自分の言葉で具体的に説明できた
      - `partial`: 触れたが理解が浅い・曖昧、または AI 主導で進んだ
      - `uncovered`: 対話では言及されなかったが、このトピックの重要な隣接観点として明示
    - `children`: サブ観点。最大 2 階層まで（ルート→子→孫）。それ以上深くしない

## 構成方針
1. **対話で実際に扱われた観点を中心に据える**。捏造しない
2. ただし、`uncovered` として「このトピックで本来扱うべきだったが対話で漏れた重要観点」を 1〜3 件含めてよい
   （ユーザーが振り返り時に未学習領域を把握できるようにするため）
3. 観点の粒度は揃える。「データ系全般」と「BTree インデックス」のような階層違いを同列にしない
4. 観点数の上限を守る（ルート直下 3〜7、各観点の children は 0〜4）
5. 観点名は名詞句で簡潔に。動詞句や疑問文にしない
6. `summary` は対話の事実に基づく。ユーザーが触れなかった内容は書かない
   （`uncovered` の場合は 1 文で「対話では扱われなかった」と書く）

## 厳守事項
- 対話が不十分（ユーザーが「わかりません」が大半 / 自分の言葉で説明していない）の場合:
  - `aspects` は空配列 `[]` を返す
  - root のみ設定する
- AI が説明した内容を「ユーザーがカバーした」と扱わない（`covered` にしない）
- 観点を無理に増やさない。対話の実態に即した数だけ出す
"""
